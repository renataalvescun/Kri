// CADASTRO E LOGIN

// Array para armazenar os usuários cadastrados
var users = [];

// Função para cadastrar um novo usuário
function registerUser(event) {
  event.preventDefault(); // Evita o comportamento padrão do formulário

  var name = document.getElementById('name').value;
  var email = document.getElementById('email').value;
  var password = document.getElementById('password').value;

  // Verifica se o email já está cadastrado
  if (getUserByEmail(email)) {
    showMessage('Email já cadastrado');
    return;
  }

  // Cria um objeto de usuário
  var user = {
    name: name,
    email: email,
    password: password
  };

  // Adiciona o usuário ao array de usuários
  users.push(user);
  showMessage('Cadastro realizado com sucesso!');
}

// Função para autenticar o login do usuário
function loginUser(event) {
  event.preventDefault(); // Evita o comportamento padrão do formulário

  var email = document.getElementById('loginEmail').value;
  var password = document.getElementById('loginPassword').value;

  // Verifica se o usuário existe
  var user = getUserByEmail(email);
  if (!user) {
    showMessage('Usuário não encontrado');
    return;
  }

  // Verifica se a senha está correta
  if (user.password !== password) {
    showMessage('Senha incorreta');
    return;
  }

  showMessage('Login realizado com sucesso!');
}

// Função para obter um usuário pelo email
function getUserByEmail(email) {
  for (var i = 0; i < users.length; i++) {
    if (users[i].email === email) {
      return users[i];
    }
  }
  return null;
}

// Função para exibir mensagens no elemento <p>
function showMessage(message) {
  document.getElementById('message').innerText = message;
}